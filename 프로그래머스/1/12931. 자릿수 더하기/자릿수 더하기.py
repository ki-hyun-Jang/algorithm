#  자연수 n이 주어지면 각 자릿수를 더해야한다.
#  아이디어1
#  123%10
#  몫 12 나머지 3
#  12%10
#  몫 1 나머지 2
#  1%10
#  몫 0 나머지 1

# // 반복문을 통해서 조건을 걸어서 몫이 0이 될때까지 나머지를 다 더하면 되겠다.
# // n이 0이될때 까지 반복한다.
# // n의 나머지를 구해서 어디에 더한다.
# //n은 n나누기10의 몫으로 치환해준다.

def solution(n):
    result = 0
    while n != 0: 
        result = result + (n%10) 
        n = n//10
        
    return result
